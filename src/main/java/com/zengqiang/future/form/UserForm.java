package com.zengqiang.future.form;

public class UserForm {

    private Integer type;

    private String account;

    private String password;

    private String nickName;

    private Integer imageId;

    private String personalIndroduce;

    private String qqNumber;

    private String email;

    private String phone;

    private int perCode;

    public int getPerCode() {
        return perCode;
    }

    public void setPerCode(int perCode) {
        this.perCode = perCode;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public Integer getImageId() {
        return imageId;
    }

    public void setImageId(Integer imageId) {
        this.imageId = imageId;
    }

    public String getPersonalIndroduce() {
        return personalIndroduce;
    }

    public void setPersonalIndroduce(String personalIndroduce) {
        this.personalIndroduce = personalIndroduce;
    }

    public String getQqNumber() {
        return qqNumber;
    }

    public void setQqNumber(String qqNumber) {
        this.qqNumber = qqNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
